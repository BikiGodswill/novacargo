// ─── Helper to generate realistic dates ──────────────────────────────────────
function daysAgo(n) {
  const d = new Date();
  d.setDate(d.getDate() - n);
  return d.toISOString().split("T")[0];
}

function daysFromNow(n) {
  const d = new Date();
  d.setDate(d.getDate() + n);
  return d.toISOString().split("T")[0];
}

function formatDate(isoStr) {
  return new Date(isoStr).toLocaleDateString("en-US", {
    year: "numeric",
    month: "short",
    day: "numeric",
  });
}

function formatDateTime(isoStr, timeStr) {
  return `${formatDate(isoStr)} ${timeStr}`;
}

// ─── Tracking timeline builder ────────────────────────────────────────────────
function buildTimeline(status, origin, destination, startDateOffset) {
  const base = daysAgo(startDateOffset);
  const events = [];

  events.push({
    id: 1,
    status: "Order Received",
    description: `Shipment booked at ${origin} facility`,
    location: origin,
    date: formatDateTime(base, "09:14 AM"),
    completed: true,
  });

  if (
    [
      STATUS.PROCESSING,
      STATUS.IN_TRANSIT,
      STATUS.OUT_FOR_DELIVERY,
      STATUS.DELIVERED,
      STATUS.DELAYED,
    ].includes(status)
  ) {
    events.push({
      id: 2,
      status: "Package Picked Up",
      description: "Package collected from sender and processed at sorting hub",
      location: origin,
      date: formatDateTime(daysAgo(startDateOffset - 1), "02:30 PM"),
      completed: true,
    });
  }

  if (
    [
      STATUS.IN_TRANSIT,
      STATUS.OUT_FOR_DELIVERY,
      STATUS.DELIVERED,
      STATUS.DELAYED,
    ].includes(status)
  ) {
    events.push({
      id: 3,
      status: "In Transit",
      description: "Package en route to destination — cleared customs",
      location: "International Hub",
      date: formatDateTime(daysAgo(startDateOffset - 2), "11:05 AM"),
      completed: true,
    });
    events.push({
      id: 4,
      status: "Arrived at Hub",
      description: `Package arrived at ${destination} distribution center`,
      location: destination,
      date: formatDateTime(daysAgo(startDateOffset - 3), "07:45 AM"),
      completed: true,
    });
  }

  if ([STATUS.OUT_FOR_DELIVERY, STATUS.DELIVERED].includes(status)) {
    events.push({
      id: 5,
      status: "Out for Delivery",
      description: "Package loaded onto delivery vehicle",
      location: destination,
      date: formatDateTime(daysAgo(1), "08:20 AM"),
      completed: true,
    });
  }

  if (status === STATUS.DELIVERED) {
    events.push({
      id: 6,
      status: "Delivered",
      description: "Package successfully delivered to recipient",
      location: destination,
      date: formatDateTime(daysAgo(0), "01:37 PM"),
      completed: true,
    });
  }

  if (status === STATUS.DELAYED) {
    events.push({
      id: 5,
      status: "Delivery Delayed",
      description: "Delay due to severe weather conditions — rescheduled",
      location: "Regional Depot",
      date: formatDateTime(daysAgo(1), "03:00 PM"),
      completed: false,
      isAlert: true,
    });
  }

  if (status === STATUS.RETURNED) {
    events.push({
      id: 5,
      status: "Return Initiated",
      description: "Delivery failed — package returned to sender",
      location: destination,
      date: formatDateTime(daysAgo(1), "10:15 AM"),
      completed: false,
      isAlert: true,
    });
  }

  return events;
}

