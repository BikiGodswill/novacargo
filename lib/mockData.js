/** Find a shipment by tracking number (case-insensitive) */
export function findShipment(trackingNumber) {
  return (
    shipments.find(
      (s) => s.trackingNumber.toLowerCase() === trackingNumber.toLowerCase()
    ) || null
  );
}

/** Get summary stats */
export function getStats() {
  const total = shipments.length;
  const delivered = shipments.filter((s) => s.status === STATUS.DELIVERED).length;
  const inTransit = shipments.filter((s) => s.status === STATUS.IN_TRANSIT).length;
  const pending = shipments.filter(
    (s) => s.status === STATUS.PENDING || s.status === STATUS.PROCESSING
  ).length;
  const delayed = shipments.filter((s) => s.status === STATUS.DELAYED).length;
  const outForDelivery = shipments.filter(
    (s) => s.status === STATUS.OUT_FOR_DELIVERY
  ).length;

  return { total, delivered, inTransit, pending, delayed, outForDelivery };
}
